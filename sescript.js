let displayedAlumni = 6; // Number of alumni to show initially
const container = document.getElementById("alumni-container");
const seeMoreButton = document.getElementById("see-more-button");
const seeLessButton = document.getElementById("see-less-button");

// Function to render alumni cards
function renderAlumni(start, end) {
    alumniData.slice(start, end).forEach(alumnus => {
        const card = document.createElement("div");
        card.className = "alumni-card";
        card.innerHTML = `
            <img src="${alumnus.picture}" alt="${alumnus.name}" class="alumni-image">
            <h2>${alumnus.name}</h2>
            <p><strong>Batch:</strong> ${alumnus.batch}</p>
            <p><strong>Message:</strong> ${alumnus.message}</p>
            <p><strong>Position:</strong> ${alumnus.currentPosition}</p>
            <div class="alumni-links">
                <a href="${alumnus.fbUrl}" target="_blank" aria-label="Facebook profile of ${alumnus.name}">Facebook</a>
                <a href="${alumnus.linkedinUrl}" target="_blank" aria-label="LinkedIn profile of ${alumnus.name}">LinkedIn</a>
            </div>
        `;
        container.appendChild(card);
    });
}

function toggleMessage(button) {
    const preview = button.previousElementSibling.previousElementSibling; // The preview text
    const fullMessage = button.previousElementSibling; // The full message
    if (fullMessage.style.display === "none") {
        fullMessage.style.display = "inline";
        preview.style.display = "none";
        button.textContent = "See Less";
    } else {
        fullMessage.style.display = "none";
        preview.style.display = "inline";
        button.textContent = "See More";
    }
}


// Initial rendering
renderAlumni(0, displayedAlumni);

// Event listener for "See More" button
seeMoreButton.addEventListener("click", () => {
    const remainingAlumni = alumniData.length - displayedAlumni;
    const nextBatch = remainingAlumni > 2 ? displayedAlumni + 6 : alumniData.length;
    renderAlumni(displayedAlumni, nextBatch);
    displayedAlumni = nextBatch;

    if (displayedAlumni === alumniData.length) {
        seeMoreButton.style.display = "none"; // Hide "See More" when all alumni are displayed
    }
    seeLessButton.style.display = "inline-block"; // Show "See Less" button
});

// Event listener for "See Less" button
seeLessButton.addEventListener("click", () => {
    displayedAlumni = 2; // Reset to initial number
    container.innerHTML = ""; // Clear the container
    renderAlumni(0, displayedAlumni);
    seeMoreButton.style.display = "inline-block"; // Show "See More" button
    seeLessButton.style.display = "none"; // Hide "See Less" button
});
document.addEventListener("DOMContentLoaded", () => {
    const hamburger = document.querySelector(".hamburger");
    const navLinks = document.querySelector(".nav-links");

    hamburger.addEventListener("click", () => {
        navLinks.classList.toggle("show");
    });
});
